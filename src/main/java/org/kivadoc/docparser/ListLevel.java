package org.kivadoc.docparser;

import org.kivadoc.dom.Elements;
import org.kivadoc.dom.LI;

public final class ListLevel {
	public final int depth; //depth in spaces/tabs
	public final String symbols; //symbols (#*-)
	public Elements<LI> listTarget;
	
	public ListLevel(int depth, String symbols) {
		this.depth = depth;
		this.symbols = symbols;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + depth;
		result = prime * result + ((symbols == null) ? 0 : symbols.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		ListLevel other = (ListLevel) obj;
		if (depth != other.depth) {
			return false;
		}
		if (symbols == null) {
			if (other.symbols != null) {
				return false;
			}
		} else if (!symbols.equals(other.symbols)) {
			return false;
		}
		return true;
	}
	
	
}
